    public void actualizarTabla(ArrayList<Area> lista) {
        DefaultTableModel tabla = (DefaultTableModel) tblAreas.getModel();
        tabla.setRowCount(0);
        
        for (Area a : lista) {
            Object[] Fila = new Object[3];
            Fila[0] = a.getId();
            Fila[1] = a.getNombre();
            Fila[2] = a.getUbicacion();
            
            tabla.addRow(Fila);
        }
        
        lblCant.setText("" + lista.size());
    }

