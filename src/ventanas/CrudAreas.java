    private void popEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popEliminarActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(null, 
                                                     "¿Estás seguro de que quieres borrar esta Área?", 
                                                     "<html><b>CONFIRMACIÓN</b><br></html>", 
                                                     JOptionPane.YES_NO_OPTION, 
                                                     JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {
            int index = tblAreas.getSelectedRow();
            Area a = areasData.get(index);

            boolean ok = areas.eliminarArea(a.getId());
            if (ok) {
                JOptionPane.showMessageDialog(this, "El Área se ha eliminado", "", JOptionPane.INFORMATION_MESSAGE);
                areasData = areas.getTodasLasAreas();
                actualizarTabla(areasData);
            }
            else
                JOptionPane.showMessageDialog(this, "El Área no se pudo eliminar", "<html><b>¡ERROR!</b><br></html>", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_popEliminarActionPerformed

